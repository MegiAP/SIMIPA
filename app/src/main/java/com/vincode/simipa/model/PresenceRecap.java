package com.vincode.simipa.model;

public class PresenceRecap {
    private String name, rkode, rdosen;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getRkode() {
        return rkode;
    }

    public void setRkode(String rkode) {
        this.rkode = rkode;
    }

    public String getRdosen() {
        return rdosen;
    }

    public void setRdosen(String rdosen) {
        this.rdosen = rdosen;
    }
}
