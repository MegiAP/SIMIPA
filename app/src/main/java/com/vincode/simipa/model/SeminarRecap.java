package com.vincode.simipa.model;

public class SeminarRecap {
    private String name, sjudul, sdosen, sjenis;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSjudul() {
        return sjudul;
    }

    public void setSjudul(String sjudul) {
        this.sjudul = sjudul;
    }

    public String getSdosen() {
        return sdosen;
    }

    public void setSdosen(String sdosen) {
        this.sdosen = sdosen;
    }

    public String getSjenis() { return sjenis; }

    public void setSjenis(String sjenis){ this.sjenis = sjenis; }
}

